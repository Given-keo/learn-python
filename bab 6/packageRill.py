# MEMANGGIL PACKAGE YANG TELAH KITA BUAT

import package.math
import package.fisika

hasil_penjumlahan = package.math.penjumlahan(3,2)
print(f'Hasil penjumlahan = {hasil_penjumlahan}')

hasil_kali = package.math.kali(5,4,3,2,1)
print(f'Hasil dari perkalian 5x4x3x2x1 = {hasil_kali}')

hasil_pangkat = package.math.pangkat(5,5)
print(f'Hasil pangkat dari 5 pangkat 5 = {hasil_pangkat}')

hasil_kecepatan = package.fisika.kecepatan(100,25)
print(f'Hasil kecepatan benda dari jarak tempuh 100m dan waktu tempuh 25 sekon adalah = {hasil_kecepatan} m')